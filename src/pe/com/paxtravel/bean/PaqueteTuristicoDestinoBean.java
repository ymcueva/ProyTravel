package pe.com.paxtravel.bean;

public class PaqueteTuristicoDestinoBean {
	private int idPaqueteDestino;
	private int idPaqueteTuristico;
	private int idDestinoCiudad;
	private int item;
	private int nuDias;
	private String feEstadia;
	private Integer idOrigen;
	
	public int getIdPaqueteDestino() {
		return idPaqueteDestino;
	}
	public void setIdPaqueteDestino(int idPaqueteDestino) {
		this.idPaqueteDestino = idPaqueteDestino;
	}
	public int getIdPaqueteTuristico() {
		return idPaqueteTuristico;
	}
	public void setIdPaqueteTuristico(int idPaqueteTuristico) {
		this.idPaqueteTuristico = idPaqueteTuristico;
	}
	public int getIdDestinoCiudad() {
		return idDestinoCiudad;
	}
	public void setIdDestinoCiudad(int idDestinoCiudad) {
		this.idDestinoCiudad = idDestinoCiudad;
	}
	public int getItem() {
		return item;
	}
	public void setItem(int item) {
		this.item = item;
	}
	public int getNuDias() {
		return nuDias;
	}
	public void setNuDias(int nuDias) {
		this.nuDias = nuDias;
	}
	public String getFeEstadia() {
		return feEstadia;
	}
	public void setFeEstadia(String feEstadia) {
		this.feEstadia = feEstadia;
	}
	public Integer getIdOrigen() {
		return idOrigen;
	}
	public void setIdOrigen(Integer idOrigen) {
		this.idOrigen = idOrigen;
	}
	

}
