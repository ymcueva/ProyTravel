package pe.com.paxtravel.bean;

public class CotizacionDetaHabitacionBean {
	
	private String idCotiza;
	private Integer idTipoHabitacion;
	private String tipoHabitacion;
	private Integer nuHabitaciones;
	public String getIdCotiza() {
		return idCotiza;
	}
	public void setIdCotiza(String idCotiza) {
		this.idCotiza = idCotiza;
	}
	public Integer getIdTipoHabitacion() {
		return idTipoHabitacion;
	}
	public void setIdTipoHabitacion(Integer idTipoHabitacion) {
		this.idTipoHabitacion = idTipoHabitacion;
	}
	public String getTipoHabitacion() {
		return tipoHabitacion;
	}
	public void setTipoHabitacion(String tipoHabitacion) {
		this.tipoHabitacion = tipoHabitacion;
	}
	public Integer getNuHabitaciones() {
		return nuHabitaciones;
	}
	public void setNuHabitaciones(Integer nuHabitaciones) {
		this.nuHabitaciones = nuHabitaciones;
	}
	
	

}
